package ru.popov.bodya.dagger2firstsample.model;


public class Speaker {
    private String name;
    private String speech;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSpeech() {
        return speech;
    }

    public void setSpeech(String speech) {
        this.speech = speech;
    }
}